								<p>Durgapur the fastest emerging city of West Bengal, India is growing to become one of the mega city of eastern India. Durgapur popularly known as the Steel city of eastern India is varying its image and emerging also in the fields of medical and education to become one of the multipurpose city of India. Durgapur has a huge base of industries with two key steel plants Durgapur Steel Plant and Alloy Steel Plant with their glowing township. There are a number of power plants, chemical and engineering industries and many small scale factories. Among them Durgapur Projects Limited (DPL), DTPS, NTPC, DVC, CMERI, Durgapur Cements, Durgapur Chemicals, Graphite India Ltd, Philips Carbon Black Ltd.(PCBL), Ultratech Cement Ltd., ALSTOM Projects India Ltd, are well known. 
